package com.inheritance;

public class A {

	public void display1()
	{
		System.out.println("one");
	}
	
	private void display2()
	{
		System.out.println("two");
	}
	
	protected void display3()
	{
		System.out.println("three");
	}
	
	void display4()
	{
		System.out.println("four");
	}
}
