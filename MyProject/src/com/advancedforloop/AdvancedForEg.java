package com.advancedforloop;

public class AdvancedForEg {

	public static void main(String[] args) 
	{
		int a[]= {2,3,4,5,6,7};
		
		for(int i:a)
		{
			System.out.println(i);
		}

	}

}
// first element of integer array a is placed in 'i' during the first iteration.
//second element placed in 'i' automatically during 2nd iteration and so on and terminates automatically. 
//Here 'i' used to hold values of 'a' in each iteration.